        "💧 *आठवड्यातील सिंचन योजना*\n\n"
        "📅 *आज ते शनि (तीव्र उष्णता, पाऊस नाही):*\n"
        "• कांदा कंद: दर ४–५ दिवसांनी हलके पाणी (जास्त पाणी नको — मान कूज)\n"
