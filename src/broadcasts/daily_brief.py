        "• चारा बाजरी: दर ५–६ दिवस\n"
        "⛔ शुक्र–शनि दुपारी ११–४ — फवारणी नाही, उन्हात काम टाळा.\n\n"
        "📅 *रवि–सोम (वाऱ्यासह ढगाळ, संक्रमण):*\n"
        "• रविवारपासून सिंचन थांबवा — नैसर्गिक आर्द्रता वाढेल\n"
